const socket = io();
$(document).ready( function() {

    $(document).on('change', '.btn-file :file', function() {
    var input = $(this),
        label = input.val().replace(/\\/g, '/').replace(/.*\//, '');
    input.trigger('fileselect', [label]);
    });

    $('.btn-file :file').on('fileselect', function(event, label) {
        
        var input = $(this).parents('.input-group').find(':text'),
            log = label;
        
        if( input.length ) {
            input.val(log);
        } else {
            if( log ) alert(log);
        }
    
    });
    function readURL(input) {
        if (input.files && input.files[0]) {
            var reader = new FileReader();
            
            reader.onload = function (e) {
                $('#img-upload').attr('src', e.target.result).height(100).width(100);
            }
            
            reader.readAsDataURL(input.files[0]);
        }
    }

    $("#imgInp").change(function(){
        readURL(this);
    }); 	

    $('.btn-register').click(function() {
        var nickname = $('#nickname').val();
        var loggedInDate = new Date();
        console.log('when submit');
        socket.emit('login', {nickname, loggedInDate});
        window.location.href = "http://localhost:3000/home?id=" + socket.id;
    });
    

});